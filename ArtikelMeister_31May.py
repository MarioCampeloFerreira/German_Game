# Constants for text formatting
GREEN_TEXT = "\033[92m"
RED_TEXT = "\033[91m"
BOLD_TEXT = "\033[1m"
RESET_TEXT = "\033[0m"

# German article rules
RULES = {
    "Der": "-or, -ling, -smus, -ig",
    "Die": "-ung, -keit, -schaft, -tät, -ik, -tion, -e, -heit, -ei",
    "Das": "-chen, -ma, -um, -ment, -lein, -tum"
}


def print_answer(article, noun, correct):
    """
    Prints the answer with the corresponding color and text formatting.
    """
    color = GREEN_TEXT if correct else RED_TEXT
    answer_type = "right" if correct else "wrong"
    print(f"{BOLD_TEXT}{color}The {answer_type} article is {article} {noun}.{RESET_TEXT}")


def display_rules():
    """
    Displays the German article rules.
    """
    print("\nGerman Article Rules / :")
    for article, rule in RULES.items():
        print(f"\"{article}\": {rule}")


def display_short_rules():
    """
    Displays the short German article rules.
    """
    print("\"Der\": -or, -ling, -smus, -ig")
    print("\"Die\": -ung, -keit, -schaft, -tät, -ik, -tion, -e, -heit, -ei")
    print("\"Das\": -chen, -ma, -um, -ment, -lein, -tum")


def end_game(score, incorrect_answers, turn):
    """
    Ends the game and displays the final score and incorrect answers.
    """
    print("End of the game!")
    print("Here are the incorrect answers:")
    for noun, details in incorrect_answers:
        article = details["article"]
        print(f"Noun: {noun}, Correct Article: {article}")
    ratio = (score / turn) * 100
    print(f"Final Score: {score}, Ratio: {ratio:.2f}%")


def start_quiz(turn, score, german_nouns, incorrect_answers):
    """
    Starts the quiz and prompts the user to choose the correct article for a given noun.
    """
    # Formatting constants
    underline = "\033[4m"
    blue_background = "\033[48;5;117m"

    # Select a noun and its details for the quiz
    if incorrect_answers and random.random() < 0.30:
        noun, details = random.choice(incorrect_answers)
    else:
        noun, details = random.choice(list(german_nouns.items()))

    # Retrieve details for the chosen noun
    article = details["article"]

    extra_info = []
    for key, value in details.items():
        if key != "article" and value != "N/A":
            extra_info.append(f"{key.capitalize()}: {value}")

    extra_info_str = ", ".join(extra_info)

    print(f"Was ist der richtige Artikel für {BOLD_TEXT}{noun}{RESET_TEXT} ({extra_info_str})?")

    user_choice = input("Wählen Sie eine Option (Der, Die, Das): ").strip()

    if user_choice.lower() == "end game":
        return True, turn, score

    if user_choice in RULES:
        turn += 1
        if user_choice == article:
            score += 1
            print(f"Richtig! Turn: {turn}, Score: {score}")
            print_answer(article, noun, correct=True)
        else:
            print_answer(article, noun, correct=False)
            print(f"Falsch..., versuchen Sie es noch einmal! Turn: {turn}, Score: {score},")
            display_short_rules()
            incorrect_answers.append((noun, details))
    else:
        print("Du kannst nur Der, Die, Das wählen")

    if turn % 5 == 0 and turn > 0:
        ratio = (score / turn) * 100
        print(f"Sie haben {score} Fragen richtig beantwortet. {BOLD_TEXT}Ihr Verhältnis ist: {underline}{blue_background} {ratio:.2f}%{RESET_TEXT}")

    return False, turn, score


